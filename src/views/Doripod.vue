                getting to know the business side of the app which is until still a bit of a challenge for me. My client
                has good reasons, business reason that they wanted to include the e-wallet functionality into the app.
                We had a total of 5 Skype calls to validate and discuss about features and also listening to my client
                explaining why they want a certain feature.</p>
